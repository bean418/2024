plt.title("한글")
plt.xlabel("X축", color = "red")
plt.ylabel("Y축", color = "#00aa00") # rgb
plt.show()

plt.plot(x, y)
plt.title("한글")
plt.xlabel("X축", loc="right") # left, center, right
plt.ylabel("Y축", loc="top") # top, center, bottom
plt.show()

